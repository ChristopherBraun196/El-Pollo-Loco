/**
 * Represents the current state of all keyboard and touch inputs.
 */
class Keyboard {
  /** @type {boolean} True while the left arrow or A key is held down. */
  LEFT;

  /** @type {boolean} True while the right arrow or D key is held down. */
  RIGHT;

  /** @type {boolean} True while the up arrow key is held down. */
  UP;

  /** @type {boolean} True while the down arrow key is held down. */
  DOWN;

  /** @type {boolean} True while the space key is held down. */
  SPACE;

  /** @type {boolean} True while the attack key (E) is held down. */
  ATTACK;
}
